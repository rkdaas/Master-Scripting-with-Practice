# Create a script that demonstrates the usage of a for loop to print numbers from 1 to 10.

#######################################Start of the script#######################################
#!/bin/bash
# Script to print numbers from 1 to 10 using a for loop
for i in {1..10}
do
    echo "Number: $i"
done
#######################################End of the script#########################################
